#include "libft.h"

void		ft_striter(char *s, void (*f)(char *))
{
	int i;

	i = 0;
	if (!s)
		return ;
	if (s)
	{
		while (s[i] != '\0')
		{
			if (!f)
				return ;
			(f(&s[i]));
			i++;
		}
	}
}
